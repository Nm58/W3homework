public class Switchstatement {
    public static void main(String[] args) {
        int car = 1;
        switch (car) {
            case 1:
                System.out.println("Volkswagen");
                break;

            case 2:
                System.out.println("Honda");
                break;

            case 3:
                System.out.println("Chevrolet");
                break;
        }
    }
}
